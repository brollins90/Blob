
namespace BMonitor.Common.Interfaces
{
    public enum UnitOfMeasure
    {
        PERCENT = 0,

        NANOSECOND = 1,
        MICROSECOND = 2,
        MILLISECOND = 3,
        SECOND = 4,

        B = 5,
        KB = 6,
        MB = 7,
        GB = 8,
        TB = 9,
    }

    // (Empty)
    // s = seconds
    // ms = milliseconds
    // us = microseconds
    // ns = nanoseconds

    // % = percentage
    // (p = percentage)

    // B = bytes
    // KB = kilobytes
    // MB = megabytes
    // GB = gigabytes
    // TB = terabytes

    // c = a continous counter (such as bytes transmitted on an interface)
}
